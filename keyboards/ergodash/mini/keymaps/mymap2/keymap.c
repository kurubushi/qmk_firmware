bool is_dvorak = false;

void layer_on_qd(int layer) {
  if (is_dvorak) {
    layer_off(_DVORAK);
  }
  return layer_on(layer);
}

void layer_off_qd(int layer) {
  layer_off(layer);
  if (is_dvorak) {
    layer_on(_DVORAK);
  }
  return;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOWER:
      if (record->event.pressed) {
        layer_on_qd(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off_qd(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on_qd(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off_qd(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on_qd(_ADJUST);
      } else {
        layer_off_qd(_ADJUST);
      }
      return false;
      break;
    case DVORAK:
      if (record->event.pressed) {
        if (is_dvorak) {
            is_dvorak = false;
            layer_off_qd(_DVORAK);
        }
        else {
            is_dvorak = true;
            layer_on_qd(_DVORAK);
        }
      }
      return false;
      break;
  }
  return true;
}
